#!/usr/bin/env node
/**
 * Regenerates lib/textPaths.ts: the "VIRTUALLY STAGED" label and
 * "STAGED · PREVIEW" watermark as SVG path outlines (DejaVu Sans Bold,
 * font size 100). Compositing outlines instead of <text> makes the output
 * identical on any server — sharp/librsvg renders <text> through fontconfig,
 * and a bare VPS with no fonts installed draws tofu boxes instead of glyphs.
 *
 * Run (dev machine with DejaVu installed, e.g. any Debian/Ubuntu box):
 *   node scripts/generate-text-paths.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import opentype from "opentype.js";

const FONT = "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf";
const OUT = path.join(path.dirname(fileURLToPath(import.meta.url)), "..", "lib", "textPaths.ts");
const FONT_SIZE = 100;

const font = opentype.parse(fs.readFileSync(FONT).buffer.slice(0));

// Glyph-by-glyph tracing (font.getPath trips on DejaVu's GSUB tables in
// opentype.js — "substFormat: 2 is not yet supported").
function trace(text, letterSpacing) {
  const scale = FONT_SIZE / font.unitsPerEm;
  const spacing = letterSpacing * FONT_SIZE;
  const combined = new opentype.Path();
  let x = 0;
  for (const ch of text) {
    const glyph = font.charToGlyph(ch);
    combined.extend(glyph.getPath(x, 0, FONT_SIZE));
    x += glyph.advanceWidth * scale + spacing;
  }
  const b = combined.getBoundingBox();
  return {
    d: combined.toPathData(2),
    x1: +b.x1.toFixed(2),
    y1: +b.y1.toFixed(2),
    x2: +b.x2.toFixed(2),
    y2: +b.y2.toFixed(2),
  };
}

const label = trace("VIRTUALLY STAGED", 0.12);
const watermark = trace("STAGED · PREVIEW", 0.02);

const body = `/**
 * GENERATED by scripts/generate-text-paths.mjs — do not edit by hand.
 *
 * Text pre-traced to SVG path outlines (DejaVu Sans Bold at font size
 * ${FONT_SIZE}) so watermarks/labels render identically on servers with no
 * fonts installed. Bounding boxes are in the same 100-unit coordinate space.
 */

export interface TextPath {
  /** SVG path data for the text outline. */
  d: string;
  x1: number;
  y1: number;
  x2: number;
  y2: number;
}

/** "VIRTUALLY STAGED" (letter-spaced) — MLS disclosure label. */
export const LABEL_TEXT: TextPath = ${JSON.stringify(label, null, 2)};

/** "STAGED · PREVIEW" — free-preview watermark tile. */
export const WATERMARK_TEXT: TextPath = ${JSON.stringify(watermark, null, 2)};
`;

fs.writeFileSync(OUT, body);
console.log(`wrote ${OUT}`);
console.log(`label bbox: ${label.x2 - label.x1} x ${label.y2 - label.y1}`);
console.log(`watermark bbox: ${watermark.x2 - watermark.x1} x ${watermark.y2 - watermark.y1}`);
